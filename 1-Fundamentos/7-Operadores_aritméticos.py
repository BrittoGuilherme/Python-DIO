# Os operadores aritméticos executam operações matemáticas, como adição, subtração com operandos.

# Adição:
print (2 + 2)

# Subtração:
print (2 - 2)

# Multiplicação:
print (5 * 5)

# Divisão:
print (20 / 5)

# Divisão Inteira: Faz operação de divisão, desprezando a sobra.
print (10 // 3)

# Módulo: Faz operação de divisão, retornando somente a sobra.
print (10 % 3)

# Exponenciação:
print (3 ** 3)