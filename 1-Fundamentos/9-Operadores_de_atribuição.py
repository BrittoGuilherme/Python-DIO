    # São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.

# Atribuição simples:

saldo = 500
print (saldo)

# Atribuição com adição:

saldo = 500
saldo += 200
print (saldo)

# Atribuição com subtração:

saldo = 500
saldo -= 200
print (saldo)

# Atribuição com multiplicação:

saldo = 500
saldo *= 2
print (saldo)

# Atribuição com divisão:

saldo = 500
saldo /= 2
print (saldo)

saldo = 500
saldo //= 3     # divisão inteira
print (saldo)

# Atribuição com módulo:

saldo = 500
saldo %= 3
print (saldo)

# Atribuição com exponenciação

saldo = 7
saldo **= 3
print (saldo)